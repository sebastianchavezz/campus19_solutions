int	ft_strlen(char *str)
{
	int		i;
	int		count;
	char	c;

	i = 0;
	count = 0;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c >= 32 && c <= 126)
		{
			count++;
		}
		i++;
	}
	return (count);
}
