#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (*(str + i) != '\0')
	{
		c = *(str + i);
		if (c >= 32 && c <= 126)
		{
			write(1, &c, 1);
		}
		i++;
	}
	write(1, "\n", 1);
}
