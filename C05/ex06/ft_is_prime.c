#include <stdio.h>

int	ft_is_prime(int nb)
{

	int i;
	
	i = 2;
	while (i < nb / 2)
	{
		if (nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}

int main (void)
{
printf("%d -> %d\n", 632, ft_is_prime(632));

}

