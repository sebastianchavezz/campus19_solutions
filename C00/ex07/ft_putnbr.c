#include <unistd.h>

void	ft_putnbr(int i)
{
	int		remain;
	char	c;

	write(1, "\"", 1);
	while (i != 0)
	{
		remain = i % 10;
		c = '0' + remain;
		write(1, &c, 1);
		i /= 10;
	}
	write(1, "\"\n", 2);
}
